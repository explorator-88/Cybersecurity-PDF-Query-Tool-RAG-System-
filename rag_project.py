from langchain.chains import RetrievalQA
from langchain_openai import ChatOpenAI
from langchain.vectorstores import FAISS
from langchain.embeddings import HuggingFaceEmbeddings

# Reload local embeddings
embedding_model = HuggingFaceEmbeddings(model_name="all-MiniLM-L6-v2")

# Reload FAISS with safe deserialization
vectorstore = FAISS.load_local("faiss_index", embedding_model, allow_dangerous_deserialization=True)

# Create retriever
retriever = vectorstore.as_retriever()

# ✅ Use new ChatOpenAI from langchain-openai
llm = ChatOpenAI(model_name="gpt-3.5-turbo", temperature=0)

# Build RAG chain
qa_chain = RetrievalQA.from_chain_type(
    llm=llm,
    retriever=retriever,
    return_source_documents=True
)


from transformers import AutoTokenizer, AutoModelForSeq2SeqLM, pipeline
from langchain.llms import HuggingFacePipeline

# Use a lightweight model compatible with Colab
model_id = "google/flan-t5-small"

# Load tokenizer and model
tokenizer = AutoTokenizer.from_pretrained(model_id)
model = AutoModelForSeq2SeqLM.from_pretrained(model_id)

# Set up text-generation pipeline
pipe = pipeline(
    "text2text-generation",
    model=model,
    tokenizer=tokenizer,
    max_new_tokens=256,
    temperature=0.7,
)

# Wrap it with LangChain
llm = HuggingFacePipeline(pipeline=pipe)

from langchain.chains import RetrievalQA

qa_chain = RetrievalQA.from_chain_type(
    llm=llm,
    retriever=retriever,
    return_source_documents=True
)

query = "What is credential dumping?"
response = qa_chain(query)

print("🔍 Question:", query)
print("\n🧠 Answer:\n", response['result'])
print("\n📄 Source:\n", response['source_documents'][0].page_content[:300])

questions = [
    "What is credential dumping?",
    "How can you mitigate lateral movement in a network?",
    "What are the privacy controls recommended by NIST?",
    "Explain access control as per NIST 800-53.",
    "What is incident response planning?"
]

for q in questions:
    response = qa_chain(q)

    print(f"\n🔍 Question: {q}")
    print(f"\n🧠 Answer:\n{response['result']}")

    print(f"\n📄 Source snippet:\n{response['source_documents'][0].page_content[:300]}...")
    print("=" * 100)