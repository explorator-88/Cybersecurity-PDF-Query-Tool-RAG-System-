from langchain_community.document_loaders import PyPDFLoader
from langchain.text_splitter import RecursiveCharacterTextSplitter

loader = PyPDFLoader("nist_800_53_r5.pdf")
pages = loader.load()

text_splitter = RecursiveCharacterTextSplitter(
    chunk_size=500,
    chunk_overlap=100
)
documents = text_splitter.split_documents(pages)

print(f"✅ Total chunks created: {len(documents)}")
print(f"📄 Sample chunk:\n\n{documents[0].page_content[:500]}")

"""Step 3"""



from langchain.embeddings import HuggingFaceEmbeddings

# Use local embedding model
embedding_model = HuggingFaceEmbeddings(model_name="all-MiniLM-L6-v2")

from langchain.vectorstores import FAISS

sample_documents = documents[:100]  # safe for local model

vectorstore = FAISS.from_documents(sample_documents, embedding_model)

vectorstore.save_local("faiss_index")

print("✅ Step 3 complete with local embeddings.")

